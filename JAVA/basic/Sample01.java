package Java.First;

public class Sample01 {
    public static void main(String[] args) {

        System.out.println("Hello Eclipse!! Hello IntelliJ");
    }
}

/*
예약어 설명
		package : 해당 클래스의 패키지 명을 나타낸다
		public : 해당 클래스나 메서드는 전체 공개된다
		class : 해당 파일이 자바의 설계도 (클래스)임을 나타낸다
		static : 해당 클래스나 메서드가 정적 클래스 혹은 정적 메서드임을 나타내다
		void : 해당 메서드는 반환 값이 없는 메서드 임을 나타낸다

식별자
		day1 - 패키지 이름
		Sample01 - 클래스 이름
		main - 메서드 이름
		String - 클래스 이름
		args - 매개변수의 이름


*/